#include "libft.h"

char	*ft_rot42(char *str)
{
	int i;
	int z;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			z = str[i];
			z = z + 16;
			if (z > 90)
				z = z - 26;
			str[i] = z;
		}
		else if (str[i] >= 'a' && str[i] <= 'z')
		{
			z = str[i];
			z = z + 16;
			if (z > 122)
				z = z - 26;
			str[i] = z;
		}
		i++;
	}
	return (str);
}
